#include "libft.h"

char	*ft_strrev(char *str)
{
	int i;
	int j;
	int r;

	i = 0;
	j = 0;
	r = 0;
	while (*(str + i) != '\0')
	{
		i++;
	}
	i--;
	while (i > j)
	{
		r = str[j];
		str[j] = str[i];
		str[i] = r;
		i--;
		j++;
	}
	return (str);
}
