#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*current;
	t_list	*headlist;

	if (!lst || !f)
		return (NULL);
	headlist = f(lst);
	if (headlist == NULL)
		return (NULL);
	current = headlist;
	while (lst->next)
	{
		lst = lst->next;
		current->next = f(lst);
		if (current->next == NULL)
			return (NULL);
		current = current->next;
	}
	return (headlist);
}
